package LeetCode.q147_对链表进行插入排序;

/**
 * @Author: Young
 * @Date: 2025/6/13 15:58
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}

