package LeetCode.q61_旋转链表;

/**
 * @Author: Young
 * @Date: 2025/12/23 01:22
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
