package LeetCode.q1019_链表中的下一个更大节点;

/**
 * @Author: Young
 * @Date: 2025/11/16 18:21
 */

public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}

