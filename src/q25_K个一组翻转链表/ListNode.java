package q25_K个一组翻转链表;

/**
 * @Author: Young
 * @Date: 2023/9/17 17:20
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
